class ListItem (var id: Int,var text: String?,var icon: Int,var type: MenuType) {

    var isNew : Boolean = false
    var subItems : List<SubItem>? = null
    constructor(ids: Int, titles: String, icons: Int, isNews: Boolean, types: MenuType) : this(ids, titles, icons, types) {
        this.isNew = isNews
    }

    constructor(ids: Int, titles: String, icons: Int, isNews: Boolean, types: MenuType, listSubItem: List<SubItem>) : this(ids, titles, icons, types) {
        this.isNew = isNews
        this.subItems = listSubItem
    }

    constructor(ids: Int, titles: String, icons: Int, types: MenuType, listSubItem: List<SubItem>) : this(ids, titles, icons, types) {
        this.subItems = listSubItem
    }
}

